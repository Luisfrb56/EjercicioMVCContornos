    private void jbAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAñadirActionPerformed
        /*
        Boton para insertar nuevo registro
        */
        int clave, nota;
        String  nombre;
        
        clave=Integer.parseInt(jtClave.getText());
        nombre=jtNombre.getText();
        nota=Integer.parseInt(jtNota.getText());
        Controlador.Metodos.nuevoAlumno(new Modelo.Alumnos(clave,nombre,nota));
        visualizarBase();
    }//GEN-LAST:event_jbAñadirActionPerformed

    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        int selecionar=jtAlumnos.getSelectedRow();
        
        if(selecionar>=0){
            Modelo.Alumnos al=Controlador.Metodos.alus.get(selecionar);
            Controlador.Metodos.eliminarAlumno(al);
            visualizarBase();
            
        }
    }//GEN-LAST:event_jbEliminarActionPerformed

    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        String buscar=jbBuscar.getText();
        if(buscar.equals("")){
            Controlador.Metodos.visualizarAlumnos();
            visualizarBase();
        }else {
            Controlador.Metodos.buscarAlumno(buscar);
            visualizarBase();
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void jbModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarActionPerformed
       int mof=jtAlumnos.getSelectedRow();
       
       alu=Controlador.Metodos.alus.get(mof);
       jtClave2.setText(Integer.toString(alu.getClave()));
       jtNombre2.setText(alu.getNombre());
       jtNota2.setText(Integer.toString(alu.getNota()));
       
    }//GEN-LAST:event_jbModificarActionPerformed

    private void jbAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAceptarActionPerformed
        Modelo.Alumnos nuevo=new Modelo.Alumnos(Integer.parseInt(jtClave2.getText()), jtNombre2.getText(), Integer.parseInt(jtNota2.getText()));
        
        Controlador.Metodos.modificarAlumno(alu, nuevo);
        visualizarBase();
        jtClave2.setText("");
        jtNombre2.setText("");
        jtNota2.setText("");
    }//GEN-LAST:event_jbAceptarActionPerformed

