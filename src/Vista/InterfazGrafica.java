    public void visualizarBase(){
        resetearTabla();
        
        for(Modelo.Alumnos alumn: Controlador.Metodos.alus){
        modelo.addRow(new Object[]{alumn.getClave(), alumn.getNombre(), alumn.getNota()});
        }
        this.jtAlumnos.setModel(modelo);
    }
    public void resetearTabla(){
        for(int i=0; i<modelo.getRowCount();i++){
            modelo.removeRow(i);
            i-=1;
        }
        jtAlumnos.setModel(modelo);
    }

